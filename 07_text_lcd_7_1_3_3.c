int main() //5*10 
{
	unsigned char name[3][16] = {{0x09, 0x15, 0x015, 0x15, 0x15, 0x09, 0x00, 0x10, 0x10, 0x1F, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
								{0x09, 0x15, 0x015, 0x15, 0x15, 0x09, 0x00, 0x10, 0x10, 0x1F, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
								{0x09, 0x15, 0x015, 0x15, 0x15, 0x09, 0x00, 0x10, 0x10, 0x1F, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}};
	unsigned char i, j;

	portOut();
	LCDInit();
	waitBusy();

	setPosition(0, 0);
	waitBusy();
	
	setCGRAM();
	
	LCD10DOTS();

	for(j=0;j<3;j++)
		for(i=0;i<16;i++)
		{
			writeChar(name[j][i]);
			_delay_ms(2);
		}
	
	setDDRAM(); //화면에 나타나게 하기위한 함수 
	
	writeChar(0x00); //0번지 
	writeChar(0x02); //1번지
	writeChar(0x04); //2번지	
	
	return 0;
}
